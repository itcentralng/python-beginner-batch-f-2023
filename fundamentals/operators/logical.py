# Logical: Used to do logic

# and: this expects the two sides of the operation to be valid

print(True and True)
print(False and True)
print(0 and 1)
print(5 and 0)
print(False and 10)
print(-1 and 10)


# or: expects only one side of the operation to be valid
print(True or True)
print(False or True)
print(0 or 1)
print(5 or 0)
print(False or 10)
print(False or 0)
print(-1 or 10)